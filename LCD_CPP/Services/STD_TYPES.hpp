#ifndef STD_TYPES_H_
#define STD_TYPES_H_

// signed
typedef char s8;
typedef short int s16;
typedef int s32;
typedef long long s64;

// unsigned
typedef unsigned char u8;
typedef unsigned short int u16;
typedef unsigned int u32;
typedef unsigned long long u64;

// float
typedef float f32;
typedef double f64;
typedef long double f128;

#define STD_OK 1
#define STD_NOK 0

#endif /* STD_TYPES_H_ */
